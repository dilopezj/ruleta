        function dibujarIndicador() {
            var ctx = miRuleta.ctx;
            ctx.strokeStyle = 'navy';
            ctx.fillStyle = 'black';
            ctx.lineWidth = 2;
            ctx.beginPath();
            ctx.moveTo(300, 0);
            ctx.lineTo(320, 0);
            ctx.lineTo(300, 40);
            ctx.lineTo(280, 0);
            ctx.stroke();
            ctx.fill();
        }

        function limpiar(){
            $('.preguntas').addClass('noneview');
            miRuleta.rotationAngle = 0;
            miRuleta.draw();       
            dibujarIndicador();  
